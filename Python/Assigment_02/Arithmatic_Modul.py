def Addition(iValue1,iValue2):
	if iValue1 < 0:
		iValue1=-iValue1
	if iValue2 < 0 :
		iValue2 =-iValue2	
	return iValue1 + iValue2

def Substraction(iValue1,iValue2):
	if iValue1 < 0:
		iValue1=-iValue1
	if iValue2 < 0 :
		iValue2 =-iValue2
	return iValue1 - iValue2;
	
def Multiplication(iValue1,iValue2):
	if iValue1 < 0:
		iValue1=-iValue1
	if iValue2 < 0 :
		iValue2 =-iValue2
	return iValue1 * iValue2

def Division(iValue1,iValue2):
	if iValue1 < 0:
		iValue1=-iValue1
	if iValue2 < 0 :
		iValue2 =-iValue2
	return iValue1 % iValue2
